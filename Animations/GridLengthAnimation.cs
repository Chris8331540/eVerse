using System;
using System.Windows;
using System.Windows.Media.Animation;

namespace eVerse.Animations
{
    /// <summary>
    /// AnimationTimeline que interpola valores de GridLength (píxeles).
    /// Soporta From/To como DependencyProperties y un EasingFunction.
    /// </summary>
    public class GridLengthAnimation : AnimationTimeline
    {
        public static readonly DependencyProperty FromProperty =
            DependencyProperty.Register(
                nameof(From),
                typeof(GridLength),
                typeof(GridLengthAnimation),
                new PropertyMetadata(default(GridLength)));

        public static readonly DependencyProperty ToProperty =
            DependencyProperty.Register(
                nameof(To),
                typeof(GridLength),
                typeof(GridLengthAnimation),
                new PropertyMetadata(default(GridLength)));

        public static readonly DependencyProperty EasingFunctionProperty =
            DependencyProperty.Register(
                nameof(EasingFunction),
                typeof(IEasingFunction),
                typeof(GridLengthAnimation),
                new PropertyMetadata(null));

        /// <summary>
        /// Valor inicial (si no está establecido se usará el valor por defecto de la propiedad animada).
        /// </summary>
        public GridLength From
        {
            get => (GridLength)GetValue(FromProperty);
            set => SetValue(FromProperty, value);
        }

        /// <summary>
        /// Valor final (si no está establecido se usará el valor por defecto de la propiedad animada).
        /// </summary>
        public GridLength To
        {
            get => (GridLength)GetValue(ToProperty);
            set => SetValue(ToProperty, value);
        }

        /// <summary>
        /// Easing opcional.
        /// </summary>
        public IEasingFunction EasingFunction
        {
            get => (IEasingFunction)GetValue(EasingFunctionProperty);
            set => SetValue(EasingFunctionProperty, value);
        }

        public override Type TargetPropertyType => typeof(GridLength);

        protected override Freezable CreateInstanceCore() => new GridLengthAnimation();

        public override object GetCurrentValue(object defaultOriginValue, object defaultDestinationValue, AnimationClock animationClock)
        {
            if (animationClock == null)
                throw new ArgumentNullException(nameof(animationClock));

            // Determinar valores "from" y "to": si el usuario no estableció From/To (valor NaN), usar valores por defecto.
            GridLength origin = (GridLength)defaultOriginValue;
            GridLength destination = (GridLength)defaultDestinationValue;

            GridLength from = (ReadLocalValue(FromProperty) == DependencyProperty.UnsetValue) ? origin : From;
            GridLength to   = (ReadLocalValue(ToProperty)   == DependencyProperty.UnsetValue) ? destination : To;

            // Solo soportamos animación en píxeles para evitar ambigüedad con Star/Auto.
            double fromValue = (from.IsAbsolute ? from.Value : origin.Value);
            double toValue = (to.IsAbsolute ? to.Value : destination.Value);

            double progress = animationClock.CurrentProgress ?? 0.0;

            if (EasingFunction != null)
                progress = EasingFunction.Ease(progress);

            double current = fromValue + (toValue - fromValue) * progress;

            return new GridLength(current, GridUnitType.Pixel);
        }
    }
}